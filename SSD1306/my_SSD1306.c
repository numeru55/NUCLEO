void OLED_put_c(uint8_t c) {
	if (c == '\n') {
		OLED_cursor_x = 0;
		OLED_cursor_y = ( OLED_cursor_y + 2 ) & ((OLED_HEIGHT>>3) - 1); // truncate 1 or 3
	} else {
		OLED_char9(c, OLED_cursor_x, OLED_cursor_y);
		OLED_cursor_x = (OLED_cursor_x + 9) & 127;
	}
}


